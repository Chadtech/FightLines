use crate::name::Name;
use crate::team_color::TeamColor;
use serde::{Deserialize, Serialize};

////////////////////////////////////////////////////////////////////////////////
// Types //
////////////////////////////////////////////////////////////////////////////////

#[derive(Serialize, Deserialize, Clone, PartialEq, Eq, Debug)]
pub struct Player {
    pub name: Name,
    pub color: TeamColor,
}

////////////////////////////////////////////////////////////////////////////////
// Api //
////////////////////////////////////////////////////////////////////////////////

impl Player {
    pub fn new(name: Name, team_color: TeamColor) -> Player {
        Player {
            name,
            color: team_color,
        }
    }
}
