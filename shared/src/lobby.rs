use crate::player::Player;
use serde::{Deserialize, Serialize};

////////////////////////////////////////////////////////////////////////////////
// Types //
////////////////////////////////////////////////////////////////////////////////

#[derive(Serialize, Deserialize, Clone, PartialEq, Eq, Debug)]
pub struct Lobby {
    host: Player,
    guests: Vec<Player>,
    game_started: bool,
}

////////////////////////////////////////////////////////////////////////////////
// Api //
////////////////////////////////////////////////////////////////////////////////

impl Lobby {
    pub fn init(host: Player) -> Lobby {
        Lobby {
            host,
            guests: Vec::new(),
            game_started: false,
        }
    }
}
